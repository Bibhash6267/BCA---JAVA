public class Literals {
    public static void main(String[] args) {
         int age =  34;
         char ch = 'A';
         float f1 = 5.6f;
         double d1 = 4.66d;
         long l1 = 566666666l;
        System.out.println(age);
        System.out.println(ch);
        System.out.println(f1);
        System.out.println(d1); 
        System.out.println(l1);
    }
}